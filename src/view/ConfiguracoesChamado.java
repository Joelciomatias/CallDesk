    private void SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarActionPerformed
        String categoria = txtDescCategoria.getText();
        boolean resultado;
        CategoriaController categoria1 = new CategoriaController();
        resultado = categoria1.inserirCategoria(categoria);
        if(resultado == true){
            JOptionPane.showMessageDialog(null, "Sucesso ao cadastrar a categoria");
            popularTabelaCategoria();
        }else{
            JOptionPane.showMessageDialog(null,"Erro ao cadastrar a categoria");
        }
    }//GEN-LAST:event_SalvarActionPerformed

    private void excluirCategoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirCategoriaActionPerformed
        int indiceLinha = tbCategoria.getSelectedRow();
        int id = (int) tbCategoria.getValueAt(indiceLinha, 0);
       
        CategoriaController cat = new CategoriaController();
        cat.deletarCategoria(id);
        popularTabelaCategoria();
    }//GEN-LAST:event_excluirCategoriaActionPerformed

    private void alterarStatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alterarStatusActionPerformed
        int indiceLinha = tbStatus.getSelectedRow();
        int id = (int) tbStatus.getValueAt(indiceLinha, 0);
        if(id == 1 || id == 2){
            JOptionPane.showMessageDialog(null, "Situações: Novo e Finalizado não podem ser alteradas ou removidas");
        }else{
        String descricao = txtStatus.getText();
        SituacaoController situacao = new SituacaoController();
        situacao.atualizarSituacao(descricao, id);
        popularTabelaStatus();
        }
    }//GEN-LAST:event_alterarStatusActionPerformed

    private void cadastrarStatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarStatusActionPerformed
       String nome = txtStatus.getText();
       SituacaoController situ = new SituacaoController();
       boolean resultado;
       resultado = situ.criarSituacao(nome);
       if(resultado == true){
            JOptionPane.showMessageDialog(null, "Sucesso ao cadastrar o status !");
            popularTabelaStatus();
        }else{
            JOptionPane.showMessageDialog(null,"Erro ao cadastrar a categoria !");
        }
    }//GEN-LAST:event_cadastrarStatusActionPerformed

