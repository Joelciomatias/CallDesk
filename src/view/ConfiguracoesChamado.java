     public void popularTabelaCategoria() {
        DefaultTableModel modelo = (DefaultTableModel) tbCategoria.getModel();
       CategoriaController categoria = new CategoriaController();
        
        modelo.setNumRows(0);
        for (Categoria cat : categoria.selecionarCategorias()) {
            modelo.addRow(new Object[]{
               cat.getId(),
               cat.getDescricao()
              

            });
           
        }
    }
     
     public void popularTabelaStatus() {
        DefaultTableModel modelo = (DefaultTableModel) tbStatus.getModel();
         SituacaoController situ = new SituacaoController();
        
        modelo.setNumRows(0);
        for (Situacao situacao : situ.selecionarSituacao()) {
            modelo.addRow(new Object[]{
               situacao.getIdSituacao(),
               situacao.getDescricao()
              

            });
          
        }
    }
  

