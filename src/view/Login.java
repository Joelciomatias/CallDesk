    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
       
        String email = txtEmail.getText();
        String senha = txtSenha.getText();
        
       ArrayList<Model.Usuario> login = user.logar(email, senha);
       
       Model.Usuario usuarioLogado = user.usuarioLogado();
       
         if(login != null){
           for(Model.Usuario usuario : user.logar(email, senha)){
               int id = usuario.getId();
               String nome = usuario.getNome();
               int tipo = usuario.getTipo();
               
               if(tipo == 1){
                   
              this.setVisible(false);
              new PrincipalADM(usuarioLogado).setVisible(true);
                            
               }if(tipo == 2){
                   this.setVisible(false);
                   
                   new PrincipalTecnico().setVisible(true);
                   
               }else{
                   this.setVisible(false);
                   
                   new PrincipalPadrao().setVisible(true);
               }
               
               UsuarioLogado userLogado = new UsuarioLogado();
               userLogado.recebeUsuarioLogado(login);
               
           }
     
       }else{
           JOptionPane.showMessageDialog(null, "Login ou senha invalidos !");
       }
          


    }//GEN-LAST:event_btnEnviarActionPerformed

